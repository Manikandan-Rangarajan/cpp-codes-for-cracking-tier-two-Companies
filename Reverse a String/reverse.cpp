#include <iostream>
using namespace std;

void reverse(string str){
    int i,j,len =str.length();
    string reversed="";
    for(i=len;i>=0;i--){
        reversed += str[i];
    }
    cout<<"The reversed string is: "<<reversed<<endl;
}

int main() {
    string str;
    cout<<"Enter a string"<<endl;
    cin>>str;
    reverse(str);
    return 0;
}
